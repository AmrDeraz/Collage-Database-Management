    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "select * from Departments where D_Name = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, search.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                String add1=rs.getString("D_Name");
                Name.setText(add1);
                String add2=rs.getString("Location");
                Location.setText(add2);
                String add3=rs.getString("INS_ID");
                INS_ID.setText(add3);
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(Departments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SearchActionPerformed

