    private void Insert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Insert1ActionPerformed
        // TODO add your handling code here:
        String name = Name.getText();
        String location = Location.getText();
        String id = INS_ID.getText();
        try{
            String sql = "Insert into Departments values ('"+name+"','"+location+"','"+id+"')";
            stmt.executeUpdate(sql);
            model.addRow(new Object[]{name,location,id});
            JOptionPane.showMessageDialog(null, "Insert Successful");
            Name.setText("");
            Location.setText("");
            INS_ID.setText("");
        }
        catch(SQLException ex){
            Logger.getLogger(Departments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Insert1ActionPerformed

